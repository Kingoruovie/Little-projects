import random
import os
list_of_numbers = []
for number in range(1, 101):
    list_of_numbers.append(number)



def number_guess():
    print(logo)
    print("\nWELCOME TO THIS NUMBER GAME")
    print("I am thinking of a number between 1 and 100 inclusive. All you have to do is guess the number\n")

    random_number = random.choice(list_of_numbers)


    first_flag = True
    while first_flag:
        difficulty_level = input("\nWhat level of difficulty of do you want? 'easy' or 'hard': ").lower()
        attempt = 0
        if difficulty_level == "easy":
            attempt = 10
            first_flag = False
        elif difficulty_level == "hard":
            attempt = 5
            first_flag = False
        else:
            print("WRONG INPUT!!! PLEASE READ CAREFULLY BEFORE TYPING ANYTHING")

    while attempt != 0:
        print(f"You have {attempt} attempt(s)")
        if attempt == 1:
            print("Please don't forget, this is your last attempt, use it wisely")
        guess = int(input("\nGuess the number: "))
        attempt -= 1
        if guess == random_number:
            print("Good job, you got the number")
            attempt = 0
        elif guess != random_number:
            if guess < random_number:
                print("TOO LOW")
            elif guess > random_number:
                print("TOO HIGH")
            if attempt != 0:
                print("That is not right, try again")
            else:
                print(f"The number is {random_number}\n")
                print("GAME OVER")

        if attempt == 0:
            decision = input("Do you want to play again? 'yes' or 'no': ").lower()
            if decision == "yes":
                os.system('cls')
                number_guess()
            else:
                print("GOODBYE, NICE HAVING YOU PLAY")

number_guess()